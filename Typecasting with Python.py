print(type(int("12")))     # String -> Int
print(type(float("12")))   # String -> Float

print(type(float(12)))     # Int -> Float
print(type(str(12)))       # Int -> Str

print(type(int(12.12)))    # Float -> Int
print(type(str(12.12)))    # Float to String


